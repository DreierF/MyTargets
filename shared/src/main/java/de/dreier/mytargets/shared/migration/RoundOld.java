package de.dreier.mytargets.shared.migration;


public class RoundOld implements IIdSettableOld {
    public long training;
    public RoundTemplateOld info;
    public String comment;
    public int reachedPoints;
    protected long id;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    @Override
    public boolean equals(Object another) {
        return another instanceof RoundOld &&
                getClass().equals(another.getClass()) &&
                id == ((RoundOld) another).id;
    }
}
